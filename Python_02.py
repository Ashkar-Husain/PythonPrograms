# Type conversion in python

# 1. Implicit type conversion - Automatic type conversion
# 2. Explicit type conversion - Manually type conversion

# lets look some examples:

# Implicit type conversion at run time:-
a = 5
print(type(a)) # type of a is <class int>

a = 67.5
print(type(a))  # type of a is <class float>

# Explicit type conversion manually:-

b = 9
print(type(b)) # type of b is <class int>

c = float(b)
print(type(c)) # type of b is <class float>
